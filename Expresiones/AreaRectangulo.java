/**
*<p>Clase AreaRectangulo.</p>
*<p>La fórmula del área de un rectángulo es b * h, siendo b la base y h la altura</p>
*<p>Calcula y muestra el área de un rectángulo, siendo b=2 y h=7.</p>
*
*@author Francisco Ramirez Ruiz
*/
class AreaRectangulo{

	public static void main(String[] args){
		//Declaracion de variables
		int area;
		int base=2;
		int altura=7;

		//calculo del area
		area=base*altura;
		
		//Se imprime por pantalla
		System.out.println("El area del rectangulo es: "+area);



	}
}